class Solution {
    public List<Integer> preorder(Node root) {
        List<Integer> result = new ArrayList<>();
        preNode(result, root);
        return result;
    }

    public void preNode(List<Integer> result, Node root){
        if(null == root){
            return;
        }
        result.add(root.val);
        List<Node> children = root.children;
        for(Node node : children){
            preNode(result, node);
        }
    }
}
